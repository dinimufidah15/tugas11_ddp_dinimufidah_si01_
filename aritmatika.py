def penjumlahan(bil1, bil2):
    hasil = bil1 + bil2
    print("hasil penjumlahan: ", hasil)

def pengurangan(bil1, bil2):
    hasil = bil1 - bil2 
    print("hasil pengurangan: ", hasil)

def perkalian(bil1, bil2):
    hasil = bil1 * bil2
    print("hasil perkalian: ", hasil)

def pembagian(bil1, bil2):
    hasil = bil1 / bil2
    print("hasil pembagian: ", hasil)

def pembagian_bulat(bil1, bil2):
    hasil = bil1 // bil2
    print("hasil pembagian bulat: ", hasil)

def modulus(bil1, bil2):
    hasil = bil1 % bil2
    print("hasil modulus: ", hasil)

def pangkat(bil1, bil2):
    hasil = bil1 ** bil2
    print("hasil pangkat: ", hasil)
    
def akar_kuadrat(bil1):
    hasil = sqrt(bil1)
    print("hasil akar kuadrat: ", hasil)

def perhitungan_log(bil1):
    hasil = log(bil1)
    print("hasil perhitungan log ", hasil)


def operasi_campuran(bil1, bil2, bil3):
    hasil = bil1 + bil2 * bil3
    print("hasil operasi campuran: ", hasil)

